package Alpro1.VariableDataTyeOperator.Variable;

public class Variable {
  public static void main(String[] args) {
      // Declaration and Initialization of variables
      int age = 69;             // Variable to store an integer value
      double salary = 50000.50; // Variable to store a floating-point number
      char grade = 'A';         // Variable to store a single character
      boolean isEmployed = true; // Variable to store a boolean value
      String name = "Galuh"; // Variable to store a sequence of characters (String)

      // Output the values of the variables
      System.out.println("Name: " + name);         // Prints the value of name
      System.out.println("Age: " + age);           // Prints the value of age
      System.out.println("Salary: $" + salary);    // Prints the value of salary
      System.out.println("Grade: " + grade);       // Prints the value of grade
      System.out.println("Employed: " + isEmployed); // Prints whether the person is employed
  }
}