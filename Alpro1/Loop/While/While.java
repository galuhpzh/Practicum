package Alpro1.Loop.While;

public class While {
  public static void main(String[] args) {
    int count = 1;

    while (count <= 5) {
      System.out.println("Count: " + count);
      count++;
    }

    /*
    output: 
    Count: 1
    Count: 2
    Count: 3
    Count: 4
    Count: 5
     */
    
  }
}
